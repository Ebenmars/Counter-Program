function decrease(){
    let num = document.getElementById("num");
    let number = parseInt(num.innerHTML);
    number--;
    num.innerHTML = number;
 }



function reset(){
    let num = document.getElementById("num");
    num.innerHTML = 0;
}

function increase(){
    let num = document.getElementById("num");
    let number = parseInt(num.innerHTML);
    number++;
    num.innerHTML = number;
}




  